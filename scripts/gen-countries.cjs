// Generates src/lib/countries.ts (ISO2 + Arabic name + calling code) for the
// login country dropdown. Plain Node (CJS) — run: node scripts/gen-countries.cjs
const { writeFileSync } = require("node:fs");
const path = require("node:path");
const { getCountries, getCountryCallingCode } = require("libphonenumber-js");

const ar = new Intl.DisplayNames(["ar"], { type: "region" });

const list = getCountries()
  .map((iso) => {
    let name = iso;
    try {
      name = ar.of(iso) || iso;
    } catch {
      /* keep iso */
    }
    return { iso, name, calling: getCountryCallingCode(iso) };
  })
  .sort((a, b) =>
    a.iso === "SA" ? -1 : b.iso === "SA" ? 1 : a.name.localeCompare(b.name, "ar"),
  );

const body = `// AUTO-GENERATED by scripts/gen-countries.cjs — do not edit by hand.
export interface Country { iso: string; name: string; calling: string }
export const DEFAULT_COUNTRY = "SA";
export const COUNTRIES: Country[] = ${JSON.stringify(list, null, 2)};
`;

writeFileSync(path.join(__dirname, "..", "src", "lib", "countries.ts"), body, "utf8");
console.log(`Wrote src/lib/countries.ts with ${list.length} countries (default SA).`);
